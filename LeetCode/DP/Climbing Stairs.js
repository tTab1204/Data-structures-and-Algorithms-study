// DP 연습

// DP는 점화식을 잘 세워야 합니다

var climbStairs = function (n) {
  let dp = [];
  dp[1] = 1;
  dp[2] = 2;

  for (let i = 3; i <= n; i++) dp[i] = dp[i - 1] + dp[i - 2];
  return dp[n];
};

